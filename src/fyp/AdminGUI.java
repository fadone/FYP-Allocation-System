    public void updateAllocTable(){
        try{
        int CC;
            ResultSet rs = access.read_students();
            ResultSetMetaData RSMD = rs.getMetaData();
            CC = RSMD.getColumnCount();
            DefaultTableModel DFT = (DefaultTableModel) allocTable.getModel();
            DFT.setRowCount(0);
            while (rs.next()) {
                Vector v2 = new Vector();
           
                for (int ii = 1; ii <= CC; ii++) {
                    int sup_id = rs.getInt("supervisor_id");
                    if(sup_id==0){
                    return;
                    }
                    
                    String std_name = rs.getString("name");
                    ResultSet sup = access.search_supervisor(rs.getInt("supervisor_id"));
                    String sup_name = "";
                    if(sup.next()) sup_name = sup.getString("name");
                    v2.add(std_name);
                    v2.add(sup_name);
                }
                DFT.addRow(v2);
            }
        } catch (Exception e) {
             e.printStackTrace();
        }
    }


