    private void allocBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_allocBtnActionPerformed
         String stdName = null;
        int stdID = -1;
        while(stdName==null){
        stdID = Integer.parseInt(JOptionPane.showInputDialog(null, "Enter student ID you want to allocate:"));
        try{
        ResultSet std = access.search_student(stdID);
        if(std.next())
        stdName = std.getString("name");
        }catch(SQLException e){e.printStackTrace();}  
        }   
        String supName = null;
        int supID = -1;
        while(supName==null){
        supID = Integer.parseInt(JOptionPane.showInputDialog(null, "Enter supervisor ID you want to allocate to:"));
        try{
        ResultSet sup = access.search_supervisor(supID);
        if(sup.next())
        supName = sup.getString("name");
        }catch(SQLException e){e.printStackTrace();}  
        }   
        access.update_std_sup(stdID, supID);
        updateAllocTable();
       // TODO add your handling code here:
    }//GEN-LAST:event_allocBtnActionPerformed

